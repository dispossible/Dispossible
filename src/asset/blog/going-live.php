<p>
    If you're anything like me your hard drive is an ever increasing pile of half complete projects, each new project coming with fresh new ideas and ambitions.
    This very blog being my current project, which as you are now reading this has finally made the move to being &quot;finished&quot;.
</p>

<p>
    You may have noticed the quote marks floating ominously around the end of that last paragraph, so let me qualify my meaning.
</p>

<p>
    When I call my project finished, in my mind it is still mid way through development, and is likely to stay that way for months yet to come.
    I could continue developing, adding all of my planned features in and getting the design perfect before shipping.
    However this is very certainly the part of the project where everything goes wrong, because your interest will fade, or another project comes along and takes centre stage,
    and it is at that point your project is added to the pile that you will &quot;come right back to&quot;.
</p>


<h2>So when is it finished?</h2>

<p>
    This can be a tricky question, and will vary based on the specific project, but my short answer is &quot;as soon as it works&quot;.
    So what I mean by that is when the core functionality of the project is stable and working, taking this blog as my example,
    its core function is to show you the text I am typing, and the moment it can do that, it's good to go.
</p>

<p>
    At the time of writing there are multiple features of this blog that are still a glimmer on the horizon, but it is capable of showing you the words, so I can call it finished.
</p>

<p>
    This is the hardest part of any project is the point where you have to show your work to the world, and it just isn't all there yet and it could always do with some more testing.
    But this is the part where you need to pluck up your courage and stand behind your work, warts and all.
</p>


<h2>Building a foundation</h2>

<p>
    With the mentality in mind that your project will now be ready to go as soon as all the basics are covered then it can bring an alteration to your work flow and how your project is ultimately structured.
    I will always try to approach my new projects with the view of building a solid foundation that will be simple and easy to expand on and add new features to as they are completed.
</p>

<p>
    I will end my first little ramble here, hopefully having provoked a bit of thought on your approach to new projects and when to call your work finished.
</p>